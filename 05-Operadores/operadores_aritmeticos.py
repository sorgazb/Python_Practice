# Operadores Aritmeticos

a = 10
b = 3

# Suma +
suma = a + b
print(f"Suma: {suma}")

# Resta -
resta = a - b
print(f"Resta: {resta}")

# Multiplicacion *
multiplicacion = a * b
print(f"Multiplicacion: {multiplicacion}")

# Division / (devuelve un float)
division = a / b
print(f"Division: {division:.2}")

# Division entera //
division_entera = a // b
print(f"Division: {division_entera}")

# Modulo %
modulo = a % b
print(f"Modulo: {modulo}")

# Potencia **
potencia = a ** b
print(f"Potencia: {potencia}")