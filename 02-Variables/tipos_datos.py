# Tipos de datos en Python

# Entero
edad = 22
print("Edad:", edad)

# Decimal
altura = 1.90
print("Altura:", altura)

# Cadena de texto
nombre = "Sergio"
print("Nombre:", nombre)

# Tipo boolean
es_estudiante = True
print("Es estudiante?", es_estudiante)

# None, ausencia de valor
direccion = None
print("Direccion:", direccion)
