# Metodos con Cadenas

cadena1 = "Hola mundo"
print(f"Cadena original: {cadena1}")

mayusculas = cadena1.upper() # para convertir a mayusculas
print(f"Cadena en mayusculas: {mayusculas}")

minusculas = cadena1.lower() # para convertir a minusculas
print(f"Cadena en minusculas: {minusculas}")

cadena2 = " Sergio Orgaz Bravo "
print(f"Cadena original: {cadena2}")

sin_espacios = cadena2.strip() # elimina espacios en blanco al principio y final
print(f"Cadena sin espacios: {sin_espacios}")